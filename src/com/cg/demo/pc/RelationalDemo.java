package com.cg.demo.pc;

public class RelationalDemo {

	public static void main(String[] args) {

		int i = 10, j = 5;

		int mySalary = 10 + 5;
		
		System.out.println(mySalary);
		
		System.out.println(i > j);
		System.out.println(i >= j);
		System.out.println(i < j);
		System.out.println(i <= j);
		System.out.println(i == j);
		System.out.println(i != j);

	}
}
