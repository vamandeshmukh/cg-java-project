package com.cg.demo.pc;

public class IncrementDecrementDemo {

	public static void main(String[] args) {

		int i = 10;

		System.out.println(i); // 10 
		System.out.println(i++); // 10 
		System.out.println(++i); // 12 
		System.out.println(i); // 12 
		System.out.println(i--); // 12 
		System.out.println(--i); // 10 
		System.out.println(i); // 10

	}

}
