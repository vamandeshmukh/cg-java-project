/**
 * Arithmetic operators + - / * %
 */
public class ArithmeticDemo {

	public static void main(String[] args) {

		int i = 10, j = 3;

		System.out.println(i + j);
		System.out.println(i - j);
		System.out.println(i / j);
		System.out.println(i * j);
		System.out.println(i % j);

	}

}
